while True:
    a = int(input("Enter first number: "))
    b = (input("Enter first operation: "))
    c = int(input("Enter second number: "))


    if b == "+":
        print(a + c)


    elif b == "-":
        print(a - c)

    elif b == "*":
        print(a * c)

    elif b == "/":
        print(a / c)

